/**
 * Bundle the physical-model tuner's AudioWorklet processor into a self-contained
 * ES module under src/public/.
 *
 * The processor (src/tuner/worklet/tuner-processor.ts) imports the pure-TS DSP
 * cores. It cannot be loaded through Vite because Vite injects its dev client
 * into bundled modules, which breaks in AudioWorkletGlobalScope (the same reason
 * src/public/sonare-worklet.js is a hand-placed artifact). esbuild produces a
 * dependency-free classic script that `audioWorklet.addModule` can load directly.
 *
 * The output is committed so the dev server and CI need no build step; `yarn dev`
 * / `yarn build` re-run this first to keep it fresh.
 *
 * Run: node scripts/build-tuner-worklet.mjs
 */
import { createHash } from 'node:crypto';
import { readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { build } from 'esbuild';

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const ENTRY = path.join(ROOT, 'src/tuner/worklet/tuner-processor.ts');
const OUT = path.join(ROOT, 'src/public/tuner-worklet.js');
const VERSION_OUT = path.join(ROOT, 'src/tuner/worklet/worklet-version.ts');

await build({
  entryPoints: [ENTRY],
  outfile: OUT,
  bundle: true,
  format: 'iife',
  target: 'es2022',
  platform: 'browser',
  legalComments: 'none',
  banner: {
    js: '// Generated from src/tuner/worklet/tuner-processor.ts by scripts/build-tuner-worklet.mjs. Do not edit.',
  },
  logLevel: 'info',
});

// A short content hash of the bundle. `useTunerEngine` appends it to the
// `addModule` URL so the browser always fetches the current worklet — an
// AudioWorklet module is otherwise cached hard, and a stale one keeps running
// old DSP after a rebuild.
const hash = createHash('sha256')
  .update(await readFile(OUT))
  .digest('hex')
  .slice(0, 12);
await writeFile(
  VERSION_OUT,
  `// Generated by scripts/build-tuner-worklet.mjs. Do not edit.\n` +
    `// Content hash of src/public/tuner-worklet.js, used to cache-bust addModule().\n` +
    `export const TUNER_WORKLET_VERSION = '${hash}';\n`,
);

console.log(`tuner worklet bundled -> ${path.relative(ROOT, OUT)} (v=${hash})`);
